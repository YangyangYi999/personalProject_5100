    private void findUserBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findUserBtnActionPerformed
        // TODO add your handling code here:
        int size = uad.getUserAccount().size();
        int i = 0;
        if(NameTxt.getText().equals("")){
            JOptionPane.showMessageDialog(null,"please input the username you wanna search","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else{
            for(UserAccount userAccount : uad.getUserAccount()){
                if(userAccount.getUserName().equals(NameTxt.getText())){
                    updateAccountJPanel panel =new updateAccountJPanel(CardSequenceJPanel,userAccount,pd);
                    CardSequenceJPanel.add("FindUserAccountsJPanel",panel);
                    CardLayout layout=(CardLayout)CardSequenceJPanel.getLayout();
                    layout.next(CardSequenceJPanel);
                }
                else{
                    i++;
                }
            }
            if(size == i){
                JOptionPane.showMessageDialog(null,"No matches","Warning",JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_findUserBtnActionPerformed

