    public void populateTable(){
        DefaultTableModel dtm=(DefaultTableModel)uaTbl.getModel();
        dtm.setRowCount(0);

        for(UserAccount ua:uad.getUserAccount()){
                    Object[] row =new Object[3];
                    row[0]=ua;
                    row[1]=ua.getPassword();
                    row[2]=ua.getRole();
                    
                    dtm.addRow(row);
        }
         
    }
    
