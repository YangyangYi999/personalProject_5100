    private void updateUserBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateUserBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow=uaTbl.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select a row from table first to view details","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else{
            UserAccount userAccount=(UserAccount)uaTbl.getValueAt(selectedRow, 0);
            updateAccountJPanel panel =new updateAccountJPanel(CardSequenceJPanel,userAccount,pd);
            CardSequenceJPanel.add("FindUserAccountsJPanel",panel);
            CardLayout layout=(CardLayout)CardSequenceJPanel.getLayout();
            layout.next(CardSequenceJPanel);
        }
    }//GEN-LAST:event_updateUserBtnActionPerformed

    private void deleteBtn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtn2ActionPerformed
        // TODO add your handling code here:
        int selectedRow=uaTbl.getSelectedRow();
        if(selectedRow>=0){
            int dialogButton=JOptionPane.YES_NO_OPTION;
            int dialogResult=JOptionPane.showConfirmDialog(null,"Would you like to delete the account details?","Warning",dialogButton);
            if(dialogResult==JOptionPane.YES_OPTION){
                UserAccount ua=(UserAccount)uaTbl.getValueAt(selectedRow, 0);
                uad.deleteUserAccount(ua);
                populateTable();

            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Please select a row from table first","Warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_deleteBtn2ActionPerformed


