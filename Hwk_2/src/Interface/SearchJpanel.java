        public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)BairplaneTable.getModel();
        dtm.setRowCount(0); 
        
        for(Airplane vs: ah.getAirplaneHistory())
           
        {
            if("Boeing".equals(vs.getManufacturer())){
                
            Object row[] = new Object[8];
            
            row[0] = vs.getSerialNum();
            row[1] = vs.getModelNum();
            row[2] = vs.getAirLinerstart() + "\t to \t" + vs.getAirLinerend();
            row[3] = vs.getAirport();
            row[4] = vs;
            row[5] = vs.getSeats(); 
            row[6] = vs.getYear();
            row[7] = vs.getStatus();
            
            dtm.addRow(row);}
        }
    }
        
        
       
        
