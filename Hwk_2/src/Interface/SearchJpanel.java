    private void Searchtab5BtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Searchtab5BtnActionPerformed
        // TODO add your handling code here:
            boolean isValidInteger = true;
            int i = 0;
            int size = ah.getAirplaneHistory().size();
            if(isInt(iSNumTxt.getText())== isValidInteger){
                for(Airplane vs: ah.getAirplaneHistory())
                {
                    if(vs.getSerialNum() == Integer.parseInt(iSNumTxt.getText()))
                    {
                        yearTxt1.setText(String.valueOf(vs.getYear()));
                        modelNumTxt1.setText(String.valueOf(vs.getModelNum()));
                        serialNumTxt1.setText(String.valueOf(vs.getSerialNum()));
                        manuTxt1.setText(vs.getManufacturer());
                        seatsNumTxt1.setText(String.valueOf(vs.getSeats()));
                        airportTxt1.setText(vs.getAirport());
                        airlinerTxt1.setText(vs.getAirLinerstart() +"to" + vs.getAirLinerstart());
                        statusTxt.setText(vs.getStatus());
                        certificateTxt.setText(vs.getCertificate());
                        break;
                        }
                    else {
                        i++;
                        }
                    }
                while(size == i){
                        JOptionPane.showMessageDialog(null,"No matches airplane");
                        break;
                }
            }
            
            else
            {
                JOptionPane.showMessageDialog(null,"Please input numbers only");
            }
        modelNumTxt1.setText("");
        serialNumTxt1.setText("");
        seatsNumTxt1.setText("");
        airportTxt1.setText("");
        airlinerTxt1.setText("");
        manuTxt1.setText("");
        yearTxt.setText("");
        statusTxt.setText("");
        certificateTxt.setText("");
        
    }//GEN-LAST:event_Searchtab5BtnActionPerformed

