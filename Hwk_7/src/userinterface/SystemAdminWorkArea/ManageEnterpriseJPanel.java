    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) enterpriseJTable.getModel();

        model.setRowCount(0);
        for (Network network : system.getNetworkList()) {
            for (State state : network.getStateDirectory().getStateList()) {
                for(Enterprise enterprise : state.getEnterpriseDirectory().getEnterpriseList()){
                    Object[] row = new Object[4];
                    row[0] = enterprise.getName();
                    row[1] = state.getName();
                    row[2] = network.getName();
                    row[3] = enterprise.getEnterpriseType().getValue();
                    model.addRow(row);
                }
            }
        }
    }

    private void populateComboBox() {
        
        networkJComboBox.removeAllItems();
        

        for (Network network : system.getNetworkList()) {
            networkJComboBox.addItem(network);
        }
      
        }
    
 private void populateStateComboBox(Network network){
        stateJComboBox.removeAllItems();
        
        for(State state: network.getStateDirectory().getStateList()){
            stateJComboBox.addItem(state);
            
        }
        
    }
