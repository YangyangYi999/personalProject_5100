    private void createFlightBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createFlightBtnActionPerformed
        // TODO add your handling code here:
       
        String departure=departureTxt.getText();
        String destination=destinationTxt.getText();
        String prefered=preferedDayBox.getItemAt(preferedDayBox.getSelectedIndex());
        String Date = DateTxt.getText();
        String size = Integer.toString(flightSchedule.getFlightSchedule().size()+1);
        int selectedRow=tblAirplane.getSelectedRow();
        if("".equals(departure)||"".equals(destination)||"".equals(prefered)||"".equals(Date)){
            JOptionPane.showMessageDialog(null,"Please fill out all fields!");
        }
        else{
        
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select a row from table first to set airplane for the flight","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else{
            Flight flight=flightSchedule.addFlight();
            flight.setDate(Date);
            flight.setId(size);
            flight.setPreferedDay(prefered);
            flight.setDeparture(departure);
            flight.setDestination(destination);
            flight.setAirliner(airliner);
            Airplane airplane = (Airplane) tblAirplane.getValueAt(selectedRow,0);
            flight.setAirplane(airplane);
            JOptionPane.showMessageDialog(null,"Account successfully created!");
        }
        }
    }//GEN-LAST:event_createFlightBtnActionPerformed

