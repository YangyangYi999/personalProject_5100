    public void populateFlightTable(){
        DefaultTableModel dtm=(DefaultTableModel)mngFlighttbl.getModel();
        dtm.setRowCount(0);
        for(Flight flight : flightSchedule.getFlightSchedule()){
            Object[] row =new Object[4];
            row[0]=flight;
            row[1]=flight.getDeparture();
            row[2]=flight.getDestination();
            row[3]=flight.getDate();
            dtm.addRow(row);
        }
        
    }
