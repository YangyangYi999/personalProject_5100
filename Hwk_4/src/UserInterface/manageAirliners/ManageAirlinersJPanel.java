     public void populateTable(){
        DefaultTableModel dtm=(DefaultTableModel)mngAirlinerstbl.getModel();
        dtm.setRowCount(0);
        for(Airliner airliner : airlinerdirectory.getAirliners()){
            Object[] row =new Object[4];
            row[0]=airliner;
            row[1]=airliner.getYear();
            row[2]=airliner.getWebsite();
            dtm.addRow(row);
        }
        
    }

    private void viewAirlinerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewAirlinerBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow=mngAirlinerstbl.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select a row from table first to view details","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else{
            Airliner airliner=(Airliner)mngAirlinerstbl.getValueAt(selectedRow, 0);
            ViewAirlinerJPanel panel =new ViewAirlinerJPanel(CardSequenceJPanel, airliner);
            CardSequenceJPanel.add("ViewAirlinerJpanel",panel);
            CardLayout layout=(CardLayout)CardSequenceJPanel.getLayout();
            layout.next(CardSequenceJPanel);
        }                                            
    }//GEN-LAST:event_viewAirlinerBtnActionPerformed

