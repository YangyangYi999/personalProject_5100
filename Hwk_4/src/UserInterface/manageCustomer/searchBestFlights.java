    private void bookBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookBtnActionPerformed
        // TODO add your handlstbling code here:
        int selectedRow=bestFlightstbl.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select a row from table first to book","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else{
            Flight flight=(Flight)bestFlightstbl.getValueAt(selectedRow, 0);
            bookFlights panel =new bookFlights(CardSequenceJPanel,flight,cd);
            CardSequenceJPanel.add("bookFlightsJpanel",panel);
            CardLayout layout=(CardLayout)CardSequenceJPanel.getLayout();
            layout.next(CardSequenceJPanel);
        }                                       
    }//GEN-LAST:event_bookBtnActionPerformed

