 private void populateTable(String departure,String destination,String preferedDay){
        DefaultTableModel dtm=(DefaultTableModel)bestFlightstbl.getModel();
        dtm.setRowCount(0);

        for(int i=0;i< masterScheduel.getMasterScheduel().size();i++){
            for(int k=0;k< masterScheduel.getMasterScheduel().get(i).getFlightSchedule().size();k++){
              
                Flight flight = masterScheduel.getMasterScheduel().get(i).getFlightSchedule().get(k);
                 if((departure.equals(flight.getDeparture()))&&destination.equals(flight.getDestination())&&preferedDay.equals(flight.getPreferedDay())){
                    Object[] row =new Object[6];
                    row[0]=flight;
                    row[1]=flight.getDate();
                    row[2]=flight.getPreferedDay();
                    row[3]=flight.getDeparture();
                    row[4]=flight.getDestination();
                    row[5]=flight.getAirliner().getNameOfcompany();
                    dtm.addRow(row);
                }
                 
            }
            
        }
         
    }
    
